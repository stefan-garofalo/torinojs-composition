import {
  lstat,
  mkdir,
  readFile,
  readdir,
  readlink,
  rm,
  symlink,
  writeFile,
} from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { agents, capabilityPacks } from "../subagents/manifest.mjs";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");
const sharedAgentsPath = path.join(repoRoot, ".agents/AGENTS.md");
const sharedSkillsPath = path.join(repoRoot, ".agents/skills");
const sharedHooksPath = path.join(repoRoot, ".agents/hooks");

const codexDir = path.join(repoRoot, ".codex");
const codexAgentsDir = path.join(codexDir, "agents");
const codexHooksDir = path.join(codexDir, "hooks");
const codexConfigPath = path.join(codexDir, "config.toml");
const codexAgentsMirrorPath = path.join(codexDir, "AGENTS.md");

const claudeDir = path.join(repoRoot, ".claude");
const claudeAgentsDir = path.join(claudeDir, "agents");
const claudeHooksDir = path.join(claudeDir, "hooks");
const claudeSettingsPath = path.join(claudeDir, "settings.json");
const claudeAgentsMirrorPath = path.join(claudeDir, "CLAUDE.md");
const claudeSkillsMirrorPath = path.join(claudeDir, "skills");

const cursorDir = path.join(repoRoot, ".cursor");
const cursorAgentsDir = path.join(cursorDir, "agents");
const cursorHooksDir = path.join(cursorDir, "hooks");
const cursorHooksJsonPath = path.join(cursorDir, "hooks.json");

const opencodeDir = path.join(repoRoot, ".opencode");
const opencodeAgentsDir = path.join(opencodeDir, "agents");
const opencodePluginsDir = path.join(opencodeDir, "plugins");
const opencodeAgentsMirrorPath = path.join(opencodeDir, "AGENTS.md");
const opencodeConfigPath = path.join(repoRoot, "opencode.json");

const managedHeader = [
  "# Generated by scripts/sync-subagents.mjs.",
  "# Do not edit by hand; update .agents/subagents/manifest.mjs and re-run sync.",
].join("\n");
const claudeManagedComment =
  "<!-- Generated by scripts/sync-subagents.mjs. Edit .agents/subagents/manifest.mjs instead. -->";
const cursorManagedComment =
  "<!-- Generated by scripts/sync-subagents.mjs. Edit .agents/subagents/manifest.mjs instead. -->";
const opencodeManagedComment =
  "<!-- Generated by scripts/sync-subagents.mjs. Edit .agents/subagents/manifest.mjs instead. -->";

function buildCodexAgentConfig({ hasFormatHook }) {
  const lines = [
    "[agents]",
    "max_threads = 6",
    "max_depth = 1",
    "",
    "[features]",
    "codex_hooks = true",
    "",
    "[hooks]",
  ];

  if (hasFormatHook) {
    lines.push(
      "SessionStart = [",
      '  { matcher = "startup|resume", hooks = [',
      '    { type = "command", command = "node \\"$(git rev-parse --show-toplevel)/.codex/hooks/format-edited-file.mjs\\" session-start", statusMessage = "Capturing edited-file baseline", timeout = 30 },',
      "  ] },",
      "]",
    );
  }

  if (hasFormatHook) {
    lines.push(
      "PostToolUse = [",
      '  { matcher = "Bash|apply_patch|.*apply.?patch.*|edit|write|multiedit", hooks = [',
      '    { type = "command", command = "node \\"$(git rev-parse --show-toplevel)/.codex/hooks/format-edited-file.mjs\\" post", statusMessage = "Auto-formatting edited files", timeout = 30 },',
      "  ] },",
      "]",
    );
  }

  lines.push("");
  return lines.join("\n");
}

function buildClaudeSettingsConfig({ hasFormatHook }) {
  return {
    hooks: hasFormatHook
      ? {
          PostToolUse: [
            {
              matcher: "Edit|Write|MultiEdit",
              hooks: [
                {
                  type: "command",
                  command: 'node "$CLAUDE_PROJECT_DIR"/.claude/hooks/format-edited-file.mjs claude',
                  timeout: 30,
                },
              ],
            },
          ],
        }
      : {},
  };
}

function buildCursorHooksConfig({ hasFormatHook }) {
  return {
    version: 1,
    hooks: hasFormatHook
      ? {
          afterFileEdit: [
            {
              command: "node .cursor/hooks/format-edited-file.mjs cursor",
            },
          ],
        }
      : {},
  };
}

function buildOpenCodeConfig(existingConfig, { hasFormatHook }) {
  const config = { ...existingConfig };

  if (hasFormatHook) {
    config.formatter = {
      ...(typeof config.formatter === "object" && config.formatter !== null
        ? config.formatter
        : {}),
      "harness-format-edited-file": {
        command: ["node", ".agents/hooks/format-edited-file.mjs", "files", "$FILE"],
        extensions: [".js", ".jsx", ".ts", ".tsx", ".mjs", ".cjs", ".json", ".py"],
      },
    };
  }

  return {
    $schema: "https://opencode.ai/config.json",
    ...config,
  };
}

async function readJsonFile(filePath) {
  try {
    return JSON.parse(await readFile(filePath, "utf8"));
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return {};
    }
    throw error;
  }
}

function getSkillNames(agent) {
  if (Array.isArray(agent.skills)) {
    return [...new Set(agent.skills)];
  }

  return [...new Set(agent.packs.flatMap((pack) => capabilityPacks[pack].skills))];
}

function formatList(items) {
  return items.map((item) => `- ${item}`).join("\n");
}

function relativeSkillPath(skillName) {
  const absoluteSkillDir = path.join(sharedSkillsPath, skillName);
  return path.relative(codexAgentsDir, absoluteSkillDir);
}

function yamlString(value) {
  return JSON.stringify(value);
}

function encodeJson(value) {
  return `${JSON.stringify(value, null, 2)}\n`;
}

async function readGuidance(files) {
  const entries = await Promise.all(
    files.map(async (file) => {
      const absolutePath = path.join(repoRoot, file);
      let content = "";

      try {
        content = await readFile(absolutePath, "utf8");
      } catch (error) {
        if (!(error && typeof error === "object" && "code" in error && error.code === "ENOENT")) {
          throw error;
        }

        content = [
          `Missing neutral guidance source: ${file}.`,
          "Author the scoped AGENTS.md file, create the sibling CLAUDE.md symlink mirror, then rerun .agents/scripts/sync-subagents.mjs.",
        ].join("\n");
      }

      return `### ${file}\n${content.trim()}`;
    }),
  );

  return entries.join("\n\n");
}

function buildPrompt(agent, guidance) {
  const packs = agent.packs.map((pack) => {
    const definition = capabilityPacks[pack];
    return `${pack}: ${definition.description}`;
  });

  return [
    `You are the ${agent.name} specialist for this repository.`,
    "",
    "Operate as a narrow owner for the assigned slice instead of broadening scope.",
    "",
    "Primary ownership:",
    formatList(agent.ownedPaths),
    "",
    "Preloaded capability packs:",
    formatList(packs),
    "",
    "Boundary rules:",
    formatList(agent.boundaries),
    "",
    "Scoped skill activation:",
    formatList(agent.activationInstructions ?? []),
    "",
    "Execution expectations:",
    formatList([
      "Prefer doing the assigned slice well over exploring adjacent areas.",
      "If the task clearly belongs to a different specialist, say so explicitly instead of improvising outside your scope.",
      "Keep repo boundaries intact: presentation implementation stays in remotion-presentation/, durable project knowledge stays in wiki/, and root scaffold guidance stays at the workspace boundary.",
      "If your work changes architecture, setup, contracts, decisions, or operator workflow, update the relevant wiki/project doc in the same task.",
    ]),
    "",
    "Baked-in scoped guidance:",
    guidance,
  ].join("\n");
}

function buildClaudeAgent(agent, prompt, skillNames) {
  const frontmatter = [
    "---",
    `name: ${yamlString(agent.name)}`,
    `description: ${yamlString(agent.description)}`,
    "model: inherit",
    "skills:",
    ...skillNames.map((skillName) => `  - ${skillName}`),
    "---",
    "",
  ].join("\n");

  return `${frontmatter}${claudeManagedComment}\n\n${prompt}\n`;
}

function buildCodexAgent(agent, prompt, skillNames) {
  const lines = [
    managedHeader,
    "",
    `name = ${JSON.stringify(agent.name)}`,
    `description = ${JSON.stringify(agent.description)}`,
    'model_reasoning_effort = "medium"',
    `developer_instructions = """\n${prompt}\n"""`,
  ];

  for (const skillName of skillNames) {
    lines.push("");
    lines.push("[[skills.config]]");
    lines.push(`path = ${JSON.stringify(relativeSkillPath(skillName))}`);
    lines.push("enabled = true");
  }

  return `${lines.join("\n")}\n`;
}

function buildCursorAgent(agent, prompt) {
  const frontmatter = [
    "---",
    `name: ${agent.name}`,
    `description: ${yamlString(agent.description)}`,
    "model: inherit",
    "---",
    "",
  ].join("\n");

  return `${frontmatter}${cursorManagedComment}\n\n${prompt}\n`;
}

function buildOpenCodeAgent(agent, prompt) {
  const frontmatter = [
    "---",
    `description: ${yamlString(agent.description)}`,
    "mode: subagent",
    "---",
    "",
  ].join("\n");

  return `${frontmatter}${opencodeManagedComment}\n\n${prompt}\n`;
}

async function ensureSymlink(linkPath, targetPath) {
  const relativeTarget = path.relative(path.dirname(linkPath), targetPath);
  await mkdir(path.dirname(linkPath), { recursive: true });

  try {
    const existingStat = await lstat(linkPath);

    if (existingStat.isSymbolicLink() && (await readlink(linkPath)) === relativeTarget) {
      return;
    }
  } catch (error) {
    if (!(error && typeof error === "object" && "code" in error && error.code === "ENOENT")) {
      throw error;
    }
  }

  await rm(linkPath, { force: true, recursive: true });
  await symlink(relativeTarget, linkPath);
}

async function pathExists(filePath) {
  try {
    await lstat(filePath);
    return true;
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return false;
    }
    throw error;
  }
}

async function writeManagedFiles(directory, extension, renderedByName) {
  await mkdir(directory, { recursive: true });

  const managedNames = new Set(Object.keys(renderedByName).map((name) => `${name}.${extension}`));

  for (const filename of await readdir(directory)) {
    if (!filename.endsWith(`.${extension}`)) {
      continue;
    }

    const filePath = path.join(directory, filename);
    const existingContent = await readFile(filePath, "utf8");
    const isManaged =
      extension === "toml"
        ? existingContent.startsWith(managedHeader)
        : existingContent.includes(claudeManagedComment);

    if (isManaged && !managedNames.has(filename)) {
      await rm(filePath, { force: true });
    }
  }

  for (const [name, content] of Object.entries(renderedByName)) {
    await writeFile(path.join(directory, `${name}.${extension}`), content, "utf8");
  }
}

async function syncHookSurface() {
  await mkdir(sharedHooksPath, { recursive: true });
  await mkdir(codexHooksDir, { recursive: true });
  await mkdir(claudeHooksDir, { recursive: true });
  await mkdir(cursorHooksDir, { recursive: true });
  await mkdir(opencodePluginsDir, { recursive: true });

  for (const hookName of ["format-edited-file.mjs"]) {
    const sourcePath = path.join(sharedHooksPath, hookName);
    const targetPaths = [
      path.join(codexHooksDir, hookName),
      path.join(claudeHooksDir, hookName),
      path.join(cursorHooksDir, hookName),
      path.join(opencodePluginsDir, hookName),
    ];

    if (await pathExists(sourcePath)) {
      for (const targetPath of targetPaths) {
        await ensureSymlink(targetPath, sourcePath);
      }
      continue;
    }

    for (const targetPath of targetPaths) {
      await rm(targetPath, { force: true, recursive: true });
    }
  }
}

async function syncSkillMirrors() {
  await ensureSymlink(claudeSkillsMirrorPath, sharedSkillsPath);
}

async function syncPromptMirrors() {
  await ensureSymlink(codexAgentsMirrorPath, sharedAgentsPath);
  await ensureSymlink(claudeAgentsMirrorPath, sharedAgentsPath);
  await ensureSymlink(opencodeAgentsMirrorPath, sharedAgentsPath);
}

async function main() {
  const claudeFiles = {};
  const codexFiles = {};
  const cursorFiles = {};
  const opencodeFiles = {};

  for (const agent of agents) {
    const guidance = await readGuidance(agent.guidanceFiles);
    const prompt = buildPrompt(agent, guidance);
    const skillNames = getSkillNames(agent);

    claudeFiles[agent.name] = buildClaudeAgent(agent, prompt, skillNames);
    codexFiles[agent.name] = buildCodexAgent(agent, prompt, skillNames);
    cursorFiles[agent.name] = buildCursorAgent(agent, prompt);
    opencodeFiles[agent.name] = buildOpenCodeAgent(agent, prompt);
  }

  await mkdir(path.dirname(codexConfigPath), { recursive: true });
  await mkdir(path.dirname(claudeSettingsPath), { recursive: true });
  await mkdir(path.dirname(cursorHooksJsonPath), { recursive: true });
  const availableHooks = {
    hasFormatHook: await pathExists(path.join(sharedHooksPath, "format-edited-file.mjs")),
  };
  await writeFile(codexConfigPath, buildCodexAgentConfig(availableHooks), "utf8");
  await rm(path.join(codexDir, "hooks.json"), { force: true });
  await writeFile(
    claudeSettingsPath,
    encodeJson(buildClaudeSettingsConfig(availableHooks)),
    "utf8",
  );
  await writeFile(cursorHooksJsonPath, encodeJson(buildCursorHooksConfig(availableHooks)), "utf8");
  if (availableHooks.hasFormatHook || (await pathExists(opencodeConfigPath))) {
    await writeFile(
      opencodeConfigPath,
      encodeJson(buildOpenCodeConfig(await readJsonFile(opencodeConfigPath), availableHooks)),
      "utf8",
    );
  }
  await writeManagedFiles(claudeAgentsDir, "md", claudeFiles);
  await writeManagedFiles(codexAgentsDir, "toml", codexFiles);
  await writeManagedFiles(cursorAgentsDir, "md", cursorFiles);
  await writeManagedFiles(opencodeAgentsDir, "md", opencodeFiles);
  await syncPromptMirrors();
  await syncSkillMirrors();
  await syncHookSurface();
}

await main();
